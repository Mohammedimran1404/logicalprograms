package programs;

public class IncrementandDecrement {

	public static void main(String[] args) {
int a = 10;
int x = a++;
System.out.println("A = "+a);
System.out.println("X = "+x);
int b = 10;
int y = ++b;
System.out.println("B = "+b);
System.out.println("Y = "+y);
int k = 5;
int i=2*3+ ++k;
System.out.println("I = "+i+" k = "+k);

	}

}
